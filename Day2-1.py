# 문제 1 - 문자열 분석(단골 유형)
# 문자열이 주어질 때 숫자/소문자/대문자의 개수를 각각 출력하시오.
s = "Hello123World"

# 출력 예시
# 숫자: 3
# 소문자: 8
# 대문자: 2

# 접근 사고 과정
# 1) 문자열을 한 글자씩 확인
# 2) 글자의 종류를 판별
# 3) 각 카운트를 증가
# 문자열 = 문자들의 리스트처럼 생각

def analyze_string(s):
    digit_count = 0     # 숫자 개수
    lower_count = 0     # 소문자 개수
    upper_count = 0     # 대문자 개수

    # 문자열은 한 글자씩 순회 가능
    for ch in s:
        if ch.isdigit():        # 숫자인 경우
            digit_count += 1
        elif ch.islower():      # 소문자인 경우
            lower_count += 1
        elif ch.isupper():      # 대문자인 경우
            upper_count += 1

    print("숫자:", digit_count)
    print("소문자:", lower_count)
    print("대문자:", upper_count)

s = "Hello123World"
analyze_string(s)

# 시험에서 쓰는 설명
# "문자열을 한 글자씩 순회하면서 isdigit/islower/isupper로 문자 종류를 판별했습니다."

