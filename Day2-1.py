# 문제 2 - 조건 구현(진짜 많이 나옴)
# 정수 배열이 주어질 때 짝수는 그대로, 홀수는 2배로 만든 새 리스트를 반환하시오
arr = [1, 2, 3, 4, 5]

# 출력 예시
# [2, 2, 6, 4, 10]

# 접근
# 하나씩 확인
# 짝수 / 홀수 조건 분기
# 결과 리스트에 추가

def transform_numbers(arr):
    result = []     # 결과를 저장할 리스트

    for num in arr:
        if num % 2 == 0:        # 짝수
            result.append(num)
        else:                   # 홀수
            result.append(num * 2)

    return result


arr = [1, 2, 3, 4, 5]
print(transform_numbers(arr))

# 시험 한 줄 설명
# "짝수/홀수를 조건문으로 분기해 결과 리스트에 누적했습니다."

